package util;

/**
 * Enum representing the available contact types for communication preferences.
 * This is typically used to specify how users prefer to receive notifications
 * or communications.
 *
 * @author Zimeng Wang
 * @author Wenxin Li
 * @author Mengying Liu
 * @since 1.0
 * @version 1.5
 */
public enum ContactType {
    /**
     * Represents the preference to receive communications via text messages.
     */
    TEXT,
    /**
     * Represents the preference to receive communications via email.
     */
    EMAIL
}
